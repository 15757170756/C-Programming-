/***************************************************** 
   冒泡排序：相邻两个数比较大小,但是如果一开始就是从小到大的数列，
   计算机还是会比较，所以可以设置标志位，当不发生交换
   时跳出循环 
******************************************************/
void Bubble_Sort_1(int a[], int n)
{
	int i, j, temp, flag;
	for (i= n-1; i> 0; i--){			//外层循环比较n-1次
		flag = 0;
		for (j = 0; j< i; j++){			//内层循环从0开始到i
			if (a[j] > a[j+1])
			{
				temp = a[j];
				a[j] = a[j+1];
				a[j+1] = temp;
				flag = 1;
			}
		}
		if (flag == 0)  break;
	}
}
/**************************
   第一个数依次和后面比较，最小的放在第一
   第二小的放在第二等等； 的
***************************/
void Bubble_Sort_2(int a[], int n)
{
	int i, j, temp, flag;
	for (i= 0; i< n; i++){				//外层比较n-1次
		flag = 0;
		for (j = i+1; j< n; j++){		//从i+1开始，每次都和后面的数比较
			if (a[i] > a[j])
			{
				temp = a[j];
				a[j] = a[i];
				a[i] = temp;
				flag = 1;
			}
		}
		if (flag == 0) break;
	} 
}

/********************************************
   插入排序。可以理解为一张一张摸牌，先摸一张
   无关紧要，第二张与第一张比较，比他大，则往
   后移动一位，依次扩展。
********************************************/
void Insertion_Sort(int a[], int n)
{
	int i, j, temp;
	for (i = 1; i < n; i++){
		temp = a[i];  //先记下手中的牌（要插入的数）
		for (j = i; (j > 0) && (a[j-1] > temp); j--)  //从原来的那张牌位置的前一个开始，如果比要插入的数大，
			a[j] = a[j-1];					//则摸到的牌往后移，
		a[j] = temp;							//
	}
}
/************************************************
  选择排序：先找出N个数中的最小者与数组中的第一个
  数交换；再找出第二小的数与a[1]交换依次类推
************************************************/
void Select_Sort(int *a, int n)
{
	int i, j, k, t;
	for (i = 0; i< n-1; i++){
		k = i;
		for (j= i+1; j< n; j++)
			if (a[j] < a[k])
				k = j;
		if (k != i){
			t = a[k];
			a[k] = a[i];
			a[i] = t;
		}
	}
}

